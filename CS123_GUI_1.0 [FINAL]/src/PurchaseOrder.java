    public void addPO()
    {
        int purchaseOrderNumber = -1;
        try{
               purchaseOrderNumber = Integer.parseInt(jTextField1.getText());
        }
        catch(NumberFormatException e){
            jErrorInvalidInput.showMessageDialog(this, "\"PO Number\" must be a number. ", "Error", jErrorInvalidInput.ERROR_MESSAGE);
        }
             
        String supplierName = jTextField4.getText();
        
        Statement insert = null;
        if (purchaseOrderNumber!=-1){
        try {
            // TEXT BOXES
            double totalPrice = 0;
            if ((jTable2.getValueAt(0, 2))!=null)
                totalPrice= Double.parseDouble("" + jTable2.getValueAt(0, 2));
            
            String query1 = "INSERT INTO purchase_order (PO_No, Supplier_Name, Total_Price)" + " values (?, ?, ?)";
            PreparedStatement ps1 = CreateConnection.connection.prepareStatement(query1);
            ps1.setInt(1, purchaseOrderNumber);
            ps1.setString(2, supplierName);
            ps1.setDouble(3, totalPrice);
            ps1.execute();
            
            // TABLE 1
            String modelNumber = "";
            int quantity = 0;
            String unit = "";
            int rowNum = 0;
            while(jTable1.getValueAt(rowNum, 0) != null)
            {
                System.out.println("fe");
                modelNumber = "" + jTable1.getValueAt(rowNum, 0);
                if (jTable1.getValueAt(rowNum, 1)!=null)
                    quantity = Integer.parseInt("" + jTable1.getValueAt(rowNum, 1));
                unit = "" + jTable1.getValueAt(rowNum, 2);
                rowNum++;
            }
            String query2 = "INSERT INTO po_quantity (Model_No, Quantity, Unit, PO_No)" + " values (?, ?, ?, ?)";
            PreparedStatement ps2 = CreateConnection.connection.prepareStatement(query2);
            ps2.setString(1, modelNumber);
            ps2.setInt(2, quantity);
            ps2.setString(3, unit);
            ps2.setInt(4, purchaseOrderNumber);
            ps2.execute();            
            
            // TABLE 2 - A
           int discountNo = 0;
            if(jTable2.getValueAt(0, 0)!=null)
                discountNo = Integer.parseInt("" + jTable2.getValueAt(0, 0));
            
            double discountRate = 0;
            if(jTable2.getValueAt(0, 1)!=null)
                discountRate = Double.parseDouble("" + jTable2.getValueAt(0, 1));   
            
            String query3 = "INSERT INTO discount_po (Discount_No, Discount_Rate, PO_No, Model_No)" + " values (?, ?, ?, ?)";
            PreparedStatement ps3 = CreateConnection.connection.prepareStatement(query3);
            ps3.setInt(1, discountNo);
            ps3.setDouble(2, discountRate);
            ps3.setInt(3, purchaseOrderNumber);
            ps3.setString(4, modelNumber);
            ps3.execute();
            
            jClientAdded.showMessageDialog(this, "Added!", "Successful", jClientAdded.INFORMATION_MESSAGE);
            clear();
        }
        catch(SQLException e1){
            e1.printStackTrace();
            System.out.println("Not added");
        }
        catch(NumberFormatException e){
            jErrorInvalidInput.showMessageDialog(this, "\"Quantity\", \"Discount\", \"Rate\", and \"Total Price\"  must be numbers. ", "Error", jErrorInvalidInput.ERROR_MESSAGE);
        }
        }
    }
    
