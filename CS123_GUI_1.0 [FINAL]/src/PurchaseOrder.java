    public void clear()
    {
        jTextField1.setText("");
        jTextField4.setText("");

        //clear tables
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        int rowCount = model.getRowCount();
        for (int i = rowCount - 1; i >= 0; i--) {
            model.removeRow(i);
        }
        model.setRowCount(20);

        model = (DefaultTableModel) jTable2.getModel();
        model.removeRow(0);
        model.setRowCount(1);
    }
